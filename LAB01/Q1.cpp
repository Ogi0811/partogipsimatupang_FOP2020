#include <iostream>
using namespace std;

int main() {

int x = 12;
int y = 5;

// penjumlahan
cout << "12 + 5 =" << x + y << endl;

//pengurangan
cout << "12 - 5 =" << x - y << endl;

//perkalian
cout << "12 * 5 =" << x * y << endl;

//pembagian
cout << "12 / 5 =" << x / y << endl;

//remainder
cout << "12 % 5 =" << x % y << endl;

return 0;

}